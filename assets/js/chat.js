function responder() {
    const input = document.getElementById("entrada").value.toLowerCase();
    let respostaEncontrada = false;
  
    // Verifica em todas as categorias
    for (let categoria in respostas) {
      for (let item of respostas[categoria]) {
        // Verifica se a pergunta contém a palavra-chave
        if (input.includes(item.pergunta.toLowerCase())) {
          exibirMensagem("Você: " + input, "usuario");
          exibirMensagem("Bot: " + item.resposta, "bot");
          respostaEncontrada = true;
          break;
        }
      }
      if (respostaEncontrada) break;
    }
  
    // Se não encontrar nenhuma resposta, avisa ao usuário
    if (!respostaEncontrada) {
      exibirMensagem("Você: " + input, "usuario");
      exibirMensagem("Bot: Desculpe, ainda não sei responder a essa pergunta. Tente buscar fontes confiáveis como o site 'todamateria'.", "bot");
    }
  
    document.getElementById("entrada").value = "";  // Limpa o campo de entrada
  }
  
  function exibirMensagem(msg, tipo) {
    const chatbox = document.getElementById("chatbox");
    const div = document.createElement("div");
    div.classList.add("mensagem", tipo);
    div.textContent = msg;
    chatbox.appendChild(div);
    chatbox.scrollTop = chatbox.scrollHeight;  // Mantém o scroll para o final
  }
  
  function limparChat() {
    document.getElementById("chatbox").innerHTML = "";  // Limpa todo o chat
  }
  
  function limparChat() {
    document.getElementById("chatbox").innerHTML = "";  // Limpa todo o chat
  }
